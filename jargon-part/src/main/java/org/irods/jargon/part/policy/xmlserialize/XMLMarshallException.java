package org.irods.jargon.part.policy.xmlserialize;

public class XMLMarshallException extends Exception {

	public XMLMarshallException() {
	}

	public XMLMarshallException(String message) {
		super(message);
	}

	public XMLMarshallException(Throwable cause) {
		super(cause);
	}

	public XMLMarshallException(String message, Throwable cause) {
		super(message, cause);
	}

}
